import mongoose from 'mongoose';
import { getModel } from '../utils/dbHelper.js';

const ContactSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true
  },
  email: {
    type: String,
    required: true,
    lowercase: true,
    trim: true
  },
  phone: {
    type: String,
    trim: true
  },
  subject: {
    type: String,
    required: true,
    trim: true
  },
  message: {
    type: String,
    required: true,
    trim: true
  }
}, {
  timestamps: true
});

const ContactModel = mongoose.model('Contact', ContactSchema);
export default getModel('contacts', ContactModel);
